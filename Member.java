    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         trainerid = txttrainerid.getText();
         name = txtname.getText();
         mobileno = txtmobileno.getText();
         gender = txtgender.getText();
         weight = txtsalary.getText();
         age = txtage.getText();
         height = txtheight.getText();
         password = txtweight.getText();
        try {
            insert();
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
     }//GEN-LAST:event_jButton1ActionPerformed
    String uName = "root";
    String pass = "root";
    String query = "insert into member_detail values (?,?,?,?,?,?,?,?)";
    String url = "jdbc:mysql://localhost:3306/user";
    String trainerid,name,mobileno,gender,password,age,height,weight;
        private void insert() throws SQLException {
            // declaration
            Connection con = DriverManager.getConnection(url, uName, pass);
            System.out.println("Connected successfully");
            PreparedStatement smt;
            smt = con.prepareCall(query);

            smt.setString(1, trainerid);
            smt.setString(2, name);
            smt.setString(3, mobileno);
            smt.setString(4, gender);
            smt.setString(5, weight);
            smt.setString(6, age);
            smt.setString(7, height);
            smt.setString(8, password);
            smt.executeUpdate();
            System.out.println("Record added successfully");
            smt.close();
            con.close();
            new MemberAttandance().setVisible(true);
        }
